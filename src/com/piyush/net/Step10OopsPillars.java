/*
Abstraction: Hiding internal complexity and showing only essential features.
        Interview Tip: Know the difference between Abstract Classes (partial abstraction) and Interfaces (100% abstraction).
Encapsulation: Bundling data (variables) and methods into a single unit (class) while restricting direct access.
        Implementation: Use private fields with public getters and setters.
Inheritance: One class acquiring the properties of another.
        Key Detail: Java does not support multiple inheritance with classes (to avoid the Diamond Problem) but allows it through Interfaces.
Polymorphism: One entity taking many forms.
        Compile-time (Static): Method Overloading.
        Runtime (Dynamic): Method Overriding.
*/